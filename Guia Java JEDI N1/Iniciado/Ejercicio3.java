package Iniciado;

public class Ejercicio3 {
    public static void main(String[] args) {
        char letra1 = 'G';
        char letra2 = 'U';
        char letra3 = 'I';
        char letra4 = 'D';
        char letra5 = 'O';

        System.out.println(letra1 + " " + letra2 + " " + letra3 + " " + letra4 + " " + letra5);
    }
}

//Cuando se utiliza una cadena de texto en comillas dobles ("), se crea un objeto de tipo String. Sin embargo, cuando se utilizan comillas simples, se crea un valor de tipo char.
